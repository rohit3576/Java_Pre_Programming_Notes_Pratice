public class Vowel {
    static char x = 'B';

    public static void main(String[] args) {
        if (x=='a' || x=='A' ||
            x=='e' || x=='E' ||
            x=='i' || x=='I' ||
            x=='o' || x=='O' ||
            x=='u' || x=='U') {

            System.out.println("It is a vowel");
        } else {
            System.out.println("It is not a vowel");
        }
    }
}
