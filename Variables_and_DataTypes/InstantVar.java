// ### 8️⃣ Instance Variable
// Create a class with:
// - An instance variable `count`
// - Create an object and print the variable

class InstantVar {
    int count = 42; // Instance variable

    public static void main(String[] args) {
        InstantVar obj = new InstantVar(); // Create an object of the class
        System.out.println("Count: " + obj.count); // Print the instance variable
    }
}