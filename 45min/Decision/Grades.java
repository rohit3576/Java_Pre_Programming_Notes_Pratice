class Grades{
    public static void main(String[] args) {
        int marks=105;
        if(marks>=85){
            System.out.println("A grade");
        }
        else if(marks>=75){
            System.out.println("B grade");
        }
        else if(marks>=35){
            System.out.println("C grade");
        }
        else{
            System.out.println("failed");
        }
    }
}